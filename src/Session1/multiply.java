package Session1;

public class multiply {
    public static void main(String[] args) {
        int n = 10;
        for (int i = 1; i < n+1; i++) {
            for (int j = 1; j < n+1; j++) {
                System.out.print(i * j + ", ");
            }
            System.out.println();
        }
    }
}
